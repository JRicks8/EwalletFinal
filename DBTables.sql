CREATE TABLE Expenses (
    ExpenseID varchar(255),
    LastName varchar(255),
    FirstName varchar(255),
    Address varchar(255),
    City varchar(255)
);

CREATE TABLE Income (
    IncomeID varchar(255),
    IncomeAmount int,
    Month int,
);
